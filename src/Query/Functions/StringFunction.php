namespace Webuni\DoctrineExtensions\Query\Functions;

use Doctrine\ORM\Query\AST\Functions\FunctionNode;
use Doctrine\ORM\Query\Lexer;
use Doctrine\ORM\Query\Parser;
use Doctrine\ORM\Query\SqlWalker;

/**
 * Usage : StringFunction STRING()
 */
final class StringFunction extends FunctionNode
{
    public $select;

    public function getSql(SqlWalker $sqlWalker): string
    {
        return '('.$sqlWalker->walkStringPrimary($this->select).')';
    }

    public function parse(Parser $parser): void
    {
        $parser->match(Lexer::T_IDENTIFIER);
        $parser->match(Lexer::T_OPEN_PARENTHESIS);
        $this->select = $parser->StringExpression();
        $parser->match(Lexer::T_CLOSE_PARENTHESIS);
    }
}
